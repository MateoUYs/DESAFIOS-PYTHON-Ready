# Inventario inicial de libros con códigos de identificación
inventario = [101, 45, 78, 234, 12, 89]

# Ordenamos en orden decreciente sin modificar el original usando sorted()
inventario_ordenado = sorted(inventario, reverse=True)

# Mostramos los resultados
print(f"Inventario original al 22/10/2025, 11:51 AM: {inventario}")
print(f"Inventario ordenado decrecientemente: {inventario_ordenado}")