# Lista de números ASCII
numeros_ascii = [65, 66, 67, 97, 98, 99]

# Convertimos los números a caracteres usando chr()
caracteres = [chr(num) for num in numeros_ascii]

# Mostramos los resultados
print(f"Lista de números ASCII al 22/10/2025, 11:51 AM: {numeros_ascii}")
print(f"Caracteres correspondientes: {caracteres}")