def capitalizar_cadena(cadena):
    """
    Convierte el primer carácter de cada palabra en mayúsculas.
    
    Args:
        cadena (str): Cadena de texto a capitalizar.
    
    Returns:
        str: Cadena con las primeras letras en mayúsculas.
    """
    return cadena.title()

# Ejemplo de uso
